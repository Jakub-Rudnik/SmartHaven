<h1>Regulamin Serwisu Obsługującego Urządzenia Smart</h1>

<h2>§ 1 Postanowienia Ogólne</h2>
<p>
    Niniejszy regulamin (dalej: "Regulamin") określa zasady korzystania z serwisu obsługującego urządzenia smart (dalej: "Serwis"), dostępnego pod adresem [adres strony internetowej].
</p>
<p>
    Serwis jest prowadzony przez [Nazwa firmy], z siedzibą w [Adres], zarejestrowaną w [KRS/NIP/REGON].
</p>
<p>
    Korzystanie z Serwisu oznacza akceptację niniejszego Regulaminu przez użytkownika (dalej: "Użytkownik").
</p>

<h2>§ 2 Definicje</h2>
<ul>
    <li><strong>Serwis</strong> – platforma internetowa obsługująca urządzenia smart, zapewniająca usługi diagnostyki, naprawy oraz wsparcia technicznego.</li>
    <li><strong>Użytkownik</strong> – osoba fizyczna, prawna lub jednostka organizacyjna korzystająca z usług Serwisu.</li>
    <li><strong>Urządzenie Smart</strong> – wszelkie urządzenia elektroniczne z funkcjami inteligentnymi, w tym smartfony, tablety, inteligentne zegarki, telewizory itp.</li>
    <li><strong>Usługi</strong> – działania podejmowane przez Serwis w celu wsparcia, diagnostyki i naprawy urządzeń smart, zgodnie z niniejszym Regulaminem.</li>
</ul>

<h2>§ 3 Zakres Usług</h2>
<p>Serwis oferuje następujące usługi:</p>
<ul>
    <li>Diagnostyka urządzeń smart.</li>
    <li>Naprawa urządzeń smart.</li>
    <li>Wsparcie techniczne w zakresie konfiguracji i aktualizacji oprogramowania.</li>
</ul>
<p>
    Serwis zastrzega sobie prawo do odmowy wykonania usługi w przypadku urządzeń niekompatybilnych, uszkodzonych mechanicznie w sposób uniemożliwiający diagnostykę lub w przypadku stwierdzenia naruszenia warunków gwarancji producenta.
</p>
<p>
    Informacje dotyczące cennika i terminów realizacji usług są udostępniane na stronie internetowej Serwisu.
</p>

<h2>§ 4 Obowiązki Użytkownika</h2>
<ul>
    <li>Użytkownik zobowiązuje się do korzystania z Serwisu zgodnie z obowiązującym prawem, Regulaminem oraz dobrymi obyczajami.</li>
    <li>Użytkownik powinien zapewnić, że urządzenie dostarczone do Serwisu jest wolne od nielegalnego oprogramowania, nie narusza praw osób trzecich oraz nie zawiera materiałów niezgodnych z prawem.</li>
    <li>Użytkownik odpowiada za wszelkie treści i dane znajdujące się na urządzeniu. Serwis nie ponosi odpowiedzialności za utratę danych podczas świadczenia usługi, a Użytkownik powinien dokonać kopii zapasowej przed przekazaniem urządzenia do Serwisu.</li>
</ul>

<h2>§ 5 Procedura Realizacji Usługi</h2>
<ol>
    <li>Użytkownik może zgłosić urządzenie do naprawy poprzez:
        <ul>
            <li>Formularz na stronie internetowej.</li>
            <li>Bezpośrednie dostarczenie urządzenia do punktu serwisowego.</li>
        </ul>
    </li>
    <li>Po dostarczeniu urządzenia, Serwis przeprowadza wstępną diagnostykę i informuje Użytkownika o kosztach oraz czasie realizacji usługi.</li>
    <li>Po akceptacji kosztów przez Użytkownika, Serwis przystępuje do naprawy lub konfiguracji urządzenia.</li>
    <li>W przypadku niemożności wykonania usługi z przyczyn niezależnych od Serwisu, Użytkownik zostanie niezwłocznie poinformowany o problemie oraz możliwości zakończenia lub kontynuacji usługi na innych warunkach.</li>
</ol>

<h2>§ 6 Odpowiedzialność Serwisu</h2>
<ul>
    <li>Serwis zobowiązuje się do świadczenia usług z należytą starannością i zgodnie z obowiązującymi standardami technicznymi.</li>
    <li>Serwis nie ponosi odpowiedzialności za szkody wynikłe z przyczyn niezależnych od Serwisu, w tym:
        <ul>
            <li>Utraty danych na urządzeniu.</li>
            <li>Naruszenia warunków gwarancji producenta na skutek naprawy w Serwisie.</li>
        </ul>
    </li>
    <li>Serwis nie ponosi odpowiedzialności za jakiekolwiek nieuprawnione działania osób trzecich, które mogą wpłynąć na funkcjonowanie urządzenia.</li>
</ul>

<h2>§ 7 Reklamacje</h2>
<p>
    W przypadku, gdy Użytkownik jest niezadowolony z jakości wykonanej usługi, przysługuje mu prawo do złożenia reklamacji.
</p>
<ul>
    <li>Reklamację można złożyć w ciągu 14 dni od dnia odbioru urządzenia z Serwisu. W przypadku stwierdzenia zasadności reklamacji, Serwis zobowiązuje się do bezpłatnego usunięcia usterek.</li>
    <li>Reklamacje należy zgłaszać za pomocą formularza dostępnego na stronie internetowej lub osobiście w punkcie serwisowym.</li>
</ul>

<h2>§ 8 Dane Osobowe</h2>
<p>
    Administratorem danych osobowych jest [Nazwa firmy]. Dane osobowe Użytkownika są przetwarzane wyłącznie w celu realizacji usług i zgodnie z przepisami prawa, w szczególności z RODO (Rozporządzeniem o Ochronie Danych Osobowych).
</p>
<p>
    Użytkownik ma prawo dostępu do swoich danych, ich poprawiania, usunięcia oraz zgłoszenia sprzeciwu wobec ich przetwarzania.
</p>

<h2>§ 9 Postanowienia Końcowe</h2>
<ul>
    <li>Serwis zastrzega sobie prawo do zmiany niniejszego Regulaminu. Zmiany wchodzą w życie w terminie 14 dni od ich publikacji na stronie internetowej.</li>
    <li>W sprawach nieuregulowanych niniejszym Regulaminem zastosowanie mają przepisy prawa polskiego, w tym Kodeksu Cywilnego.</li>
    <li>Regulamin obowiązuje od dnia jego publikacji na stronie internetowej.</li>
</ul>

